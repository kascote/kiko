import 'package:kiko/kiko.dart';

/// Defines the options for layout flex justify content in a container.
///
/// This enumeration controls the distribution of space when layout constraints
/// are met.
enum Flex {
  /// Fills the available space within the container, putting excess space into
  /// the last constraint of the lowest priority.
  ///
  /// The following examples illustrate the allocation of excess in various
  /// combinations of constraints. As a refresher, the priorities of constraints
  /// are as follows:
  ///
  /// 1. [ConstraintMin]
  /// 2. [ConstraintMax]
  /// 3. [ConstraintLength]
  /// 4. [ConstraintPercentage]
  /// 5. [ConstraintRatio]
  /// 6. [ConstraintFill]
  ///
  /// When every constraint is [ConstraintLength], the last element gets the
  /// excess.
  ///
  /// ```plain
  /// <----------------------------------- 80 px ------------------------------------>
  /// ┌──────20 px───────┐┌──────20 px───────┐┌────────────────40 px─────────────────┐
  /// │    Length(20)    ││    Length(20)    ││              Length(20)              │
  /// └──────────────────┘└──────────────────┘└──────────────────────────────────────┘
  ///                                         ^^^^^^^^^^^^^^^^ EXCESS ^^^^^^^^^^^^^^^^
  /// ```
  ///
  /// Fill constraints have the lowest priority amongst all the constraints and hence
  /// will always take up any excess space available.
  ///
  /// ```plain
  /// <----------------------------------- 80 px ------------------------------------>
  /// ┌──────20 px───────┐┌──────20 px───────┐┌──────20 px───────┐┌──────20 px───────┐
  /// │      Fill(0)     ││      Max(20)     ││    Length(20)    ││     Length(20)   │
  /// └──────────────────┘└──────────────────┘└──────────────────┘└──────────────────┘
  /// ^^^^^^ EXCESS ^^^^^^
  /// ```
  ///
  /// # Examples
  ///
  /// ```plain
  /// <------------------------------------80 px------------------------------------->
  /// ┌──────────────────────────60 px───────────────────────────┐┌──────20 px───────┐
  /// │                          Min(20)                         ││      Max(20)     │
  /// └──────────────────────────────────────────────────────────┘└──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  /// ┌────────────────────────────────────80 px─────────────────────────────────────┐
  /// │                                    Max(20)                                   │
  /// └──────────────────────────────────────────────────────────────────────────────┘
  /// ```
  legacy,

  /// Aligns items to the start of the container.
  ///
  /// ```plain
  /// <------------------------------------80 px------------------------------------->
  /// ┌────16 px─────┐┌──────20 px───────┐┌──────20 px───────┐
  /// │Percentage(20)││    Length(20)    ││     Fixed(20)    │
  /// └──────────────┘└──────────────────┘└──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  /// ┌──────20 px───────┐┌──────20 px───────┐
  /// │      Max(20)     ││      Max(20)     │
  /// └──────────────────┘└──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  /// ┌──────20 px───────┐
  /// │      Max(20)     │
  /// └──────────────────┘
  /// ```
  start,

  /// Aligns items to the end of the container.
  ///
  /// ```plain
  /// <------------------------------------80 px------------------------------------->
  ///                         ┌────16 px─────┐┌──────20 px───────┐┌──────20 px───────┐
  ///                         │Percentage(20)││    Length(20)    ││     Length(20)   │
  ///                         └──────────────┘└──────────────────┘└──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  ///                                         ┌──────20 px───────┐┌──────20 px───────┐
  ///                                         │      Max(20)     ││      Max(20)     │
  ///                                         └──────────────────┘└──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  ///                                                             ┌──────20 px───────┐
  ///                                                             │      Max(20)     │
  ///                                                             └──────────────────┘
  /// ```
  end,

  /// Centers items within the container.
  ///
  /// ```plain
  /// <------------------------------------80 px------------------------------------->
  ///             ┌────16 px─────┐┌──────20 px───────┐┌──────20 px───────┐
  ///             │Percentage(20)││    Length(20)    ││     Length(20)   │
  ///             └──────────────┘└──────────────────┘└──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  ///                     ┌──────20 px───────┐┌──────20 px───────┐
  ///                     │      Max(20)     ││      Max(20)     │
  ///                     └──────────────────┘└──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  ///                               ┌──────20 px───────┐
  ///                               │      Max(20)     │
  ///                               └──────────────────┘
  /// ```
  center,

  /// Adds excess space between each element.
  ///
  /// ```plain
  /// <------------------------------------80 px------------------------------------->
  /// ┌────16 px─────┐            ┌──────20 px───────┐            ┌──────20 px───────┐
  /// │Percentage(20)│            │    Length(20)    │            │     Length(20)   │
  /// └──────────────┘            └──────────────────┘            └──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  /// ┌──────20 px───────┐                                        ┌──────20 px───────┐
  /// │      Max(20)     │                                        │      Max(20)     │
  /// └──────────────────┘                                        └──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  /// ┌────────────────────────────────────80 px─────────────────────────────────────┐
  /// │                                    Max(20)                                   │
  /// └──────────────────────────────────────────────────────────────────────────────┘
  /// ```
  spaceBetween,

  /// Adds excess space around each element.
  ///
  /// ```plain
  /// <------------------------------------80 px------------------------------------->
  ///       ┌────16 px─────┐      ┌──────20 px───────┐      ┌──────20 px───────┐
  ///       │Percentage(20)│      │    Length(20)    │      │     Length(20)   │
  ///       └──────────────┘      └──────────────────┘      └──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  ///              ┌──────20 px───────┐              ┌──────20 px───────┐
  ///              │      Max(20)     │              │      Max(20)     │
  ///              └──────────────────┘              └──────────────────┘
  ///
  /// <------------------------------------80 px------------------------------------->
  ///                               ┌──────20 px───────┐
  ///                               │      Max(20)     │
  ///                               └──────────────────┘
  /// ```
  spaceAround,
}
